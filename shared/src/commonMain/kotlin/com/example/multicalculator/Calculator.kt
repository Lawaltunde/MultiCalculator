package com.example.multicalculator

class Calculator{
    fun Add(left : Int, right : Int) : Int{
        return (left + right)
    }

    fun subtract(left : Int, right : Int) : Int{
        return (left - right)
    }

    fun divide(left : Int, right : Int) : Int{
        return (left / right)
    }

    fun multiply(left : Int, right : Int) : Int{
        return (left * right)
    }
}